// Data & time in JS
let d = new Date();

console.log(d.toDateString()) // Sun Jan 05 2025

console.log(d.toString());  //  Sun Jan 05 2025 07:01:17 GMT+0000 (Coordinated Universal Time)

console.log(d.toJSON());    // 2025-01-05T07:01:37.465Z

console.log(d.toISOString());   //2025-01-05T07:03:54.689Z

console.log(d.toLocaleDateString());    // 1/5/2025

console.log(d.toLocaleString());    //  1/5/2025, 7:05:54 AM
